-- 2. Tạo các table và khóa chính
CREATE TABLE THAMGIADT
(
	MAGV NCHAR(3) NOT NULL,
	MADT NCHAR(4) NOT NULL,
	STT INT,
	PHUCAP FLOAT,
	KETQUA NVARCHAR(10),
	PRIMARY KEY (MAGV, MADT, STT)
)
GO

CREATE TABLE KHOA
(
	MAKHOA NCHAR(4) NOT NULL,
	TENKHOA NCHAR(50) DEFAULT N'TEN KHOA',
	NAMTL INT,
	PHONG CHAR(3),
	DIENTHOAI CHAR(10),
	TRUONGKHOA NCHAR(3),
	NGAYNHANCHUC DATE,
	PRIMARY KEY (MAKHOA)
)
GO

CREATE TABLE BOMON
(
	MABM NCHAR(4) NOT NULL,
	TENBM NCHAR(50) DEFAULT N'TEN BO MON',
	PHONG CHAR(3),
	DIENTHOAI CHAR(11),
	TRUONGBOMON NCHAR(3),
	MAKHOA NCHAR(4),
	NGAYNHANCHUC DATE,
	PRIMARY KEY (MABM)
)
GO

CREATE TABLE CONGVIEC
(
	MADT NCHAR(4) NOT NULL,
	SOTT INT,
	TENCV NVARCHAR(50),
	NGAYBD DATETIME,
	NGAYKT DATETIME,
	PRIMARY KEY (MADT, SOTT)
)
GO

CREATE TABLE DETAI
(
	MADT NCHAR(4) NOT NULL,
	TENDT NVARCHAR(50) DEFAULT N'TÊN ĐỀ TÀI',
	CAPQL NCHAR(20),
	KINHPHI FLOAT,
	NGAYBD DATE,
	NGAYKT DATE,
	MACD NCHAR(4),
	GVCNDT NCHAR(3),
	PRIMARY KEY (MADT)
)
GO

CREATE TABLE CHUDE
(
	MACD NCHAR(4) NOT NULL,
	TENCD NVARCHAR(30) DEFAULT N'TÊN CHỦ ĐỀ',
	PRIMARY KEY (MACD)
)
GO
	
CREATE TABLE GIAOVIEN
(
	MAGV NCHAR(3) NOT NULL,
	HOTEN NVARCHAR(50) DEFAULT N'HỌ VÀ TÊN GIÁO VIÊN',
	LUONG FLOAT,
	PHAI NCHAR(3),
	NGSINH DATE,
	DIACHI NCHAR(50),
	GVQLCM NCHAR(3),
	MABM NCHAR(4),
	PRIMARY KEY (MAGV)
)
GO
	
CREATE TABLE NGUOITHAN
(
	MAGV NCHAR(3) NOT NULL,
	TEN NCHAR(12),
	NGSINH DATETIME,
	PHAI NCHAR(3),
	PRIMARY KEY (MAGV,TEN)
)
GO
	
CREATE TABLE GV_DT
(
	MAGV NCHAR(3) NOT NULL,
	DIENTHOAI CHAR(10) NOT NULL,
	PRIMARY KEY (MAGV,DIENTHOAI)
)
GO

